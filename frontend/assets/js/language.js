  function current() {
    return localStorage.getItem(KEY) || MR3DB.getSettings().defaultLanguage || "ar";
  }

  function apply(lang) {
    const chosen = lang || current();
    localStorage.setItem(KEY, chosen);
    document.documentElement.lang = chosen;
    document.documentElement.dir = chosen === "ar" ? "rtl" : "ltr";
    document.querySelectorAll("[data-i18n]").forEach((node) => {
      node.textContent = t(node.dataset.i18n);
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
      node.setAttribute("placeholder", t(node.dataset.i18nPlaceholder));
    });
  }

  function t(key, params) {
    const lang = current();
    let value = (dict[lang] && dict[lang][key]) || dict.en[key] || key;
    if (params) {
      Object.keys(params).forEach((name) => {
        value = value.replace(new RegExp(`{${name}}`, "g"), params[name]);
      });
    }
    return value;
  }

  function isArabic() {
    return current() === "ar";
  }

  function name(row) {
    if (!row) return "";
    return isArabic() ? row.nameAr || row.name || row.nameEn || "" : row.nameEn || row.name || row.nameAr || "";
  }

  window.MR3I18n = { t, apply, current, isArabic, name, dict };
